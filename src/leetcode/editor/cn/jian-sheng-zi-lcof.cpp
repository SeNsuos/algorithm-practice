using namespace std;

//leetcode submit region begin(Prohibit modification and deletion)
class Solution {
public:
    int cuttingRope(int n) {
        if (n <= 3) return n - 1;
        // n = 3a + b;
        int a = n / 3, b = n % 3;
        // b = 0 return pow(3, a);
        // b = 1 return pow(3, a - 1) * (2 + 2);
        // b = 2 return pow(3, a) * 2;
        int res = 0;
        switch (b) {
            case 0:
                res = pow(3, a);
                break;
            case 1:
                res = pow(3, a - 1) * 4;
                break;
            case 2:
                res = pow(3, a) * 2;
                break;
        }

        return res;
    }
};

#include <iostream>
#include "vector"
#include "string"
#include "stack"
#include "unordered_map"

using namespace std;

//leetcode submit region begin(Prohibit modification and deletion)
class MinStack {
    stack<int> left, right;
public:
    /** initialize your data structure here. */
    MinStack() {
        if (!left.empty()) {
            left.pop();
        }
        if (!right.empty()) {
            right.pop();
        }
    }
    
    void push(int x) {
        if (left.empty()) {
            right.push(x);
        } else {
            int value = right.top();
            if (x <= value) {
                right.push(x);
            }
        }
        left.push(x);
    }
    
    void pop() {
        if (!right.empty() && left.top() == right.top()) {
            right.pop();
        }

        left.pop();
    }
    
    int top() {
        return left.top();
    }
    
    int min() {
        return right.top();
    }
};

/**
 * Your MinStack object will be instantiated and called as such:
 * MinStack* obj = new MinStack();
 * obj->push(x);
 * obj->pop();
 * int param_3 = obj->top();
 * int param_4 = obj->min();
 */
//leetcode submit region end(Prohibit modification and deletion)

int main() {
    MinStack* obj = new MinStack;
    obj->push(-2);
    obj->push(0);
    obj->push(-1);
    cout << obj->min() << endl;
    cout << obj->top() << endl;
    obj->pop();
    cout << obj->min() << endl;
}
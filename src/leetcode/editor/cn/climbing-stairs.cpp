class Solution {
public:
    int climbStairs(int n) {
        if (n <= 1) return 1;
        int x = 2, y = 1, z = 1;
        for (int i = 2; i <= n; ++i) {
            x = y + z;
            z = y;
            y = x;
        }

        return x;
    }
};

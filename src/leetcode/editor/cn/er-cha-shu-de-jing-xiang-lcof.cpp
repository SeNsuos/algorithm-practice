#include <iostream>
#include "vector"
#include "string"
#include "stack"
#include "unordered_map"
#include "queue"

using namespace std;

struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
};

